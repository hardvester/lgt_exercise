WITH source AS (
    SELECT
        UPPER(TRIM(BENCHMARK_ID))   AS BENCHMARK_ID,
        BENCHMARK_NAME,
        BENCHMARK_VALUE,
        RECORD_DATE,
        LOAD_DATETIME,
        RECORD_SOURCE
    FROM {{ ref('raw_benchmark_data') }}
)

SELECT
    UPPER(sha256(UPPER(TRIM(CAST(BENCHMARK_ID AS VARCHAR))))::VARCHAR) AS HK_BENCHMARK,
    UPPER(sha256(CONCAT_WS('||',
        COALESCE(UPPER(TRIM(CAST(BENCHMARK_NAME AS VARCHAR))), '^^'),
        COALESCE(UPPER(TRIM(CAST(BENCHMARK_VALUE AS VARCHAR))), '^^')
    ))::VARCHAR) AS HASHDIFF_BENCHMARK,
    BENCHMARK_ID,
    BENCHMARK_NAME,
    BENCHMARK_VALUE,
    RECORD_DATE         AS EFFECTIVE_FROM,
    LOAD_DATETIME,
    RECORD_SOURCE
FROM source
