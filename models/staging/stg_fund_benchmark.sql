WITH source AS (
    SELECT
        UPPER(TRIM(FUND_ID))        AS FUND_ID,
        UPPER(TRIM(BENCHMARK_ID))   AS BENCHMARK_ID,
        LOAD_DATETIME,
        RECORD_SOURCE
    FROM {{ ref('raw_fund_benchmark') }}
)

SELECT
    UPPER(sha256(UPPER(TRIM(CAST(FUND_ID AS VARCHAR))))::VARCHAR) AS HK_FUND,
    UPPER(sha256(UPPER(TRIM(CAST(BENCHMARK_ID AS VARCHAR))))::VARCHAR) AS HK_BENCHMARK,
    UPPER(sha256(CONCAT_WS('||',
        COALESCE(UPPER(TRIM(CAST(FUND_ID AS VARCHAR))), '^^'),
        COALESCE(UPPER(TRIM(CAST(BENCHMARK_ID AS VARCHAR))), '^^')
    ))::VARCHAR) AS HK_FUND_BENCHMARK,
    FUND_ID,
    BENCHMARK_ID,
    LOAD_DATETIME,
    RECORD_SOURCE
FROM source
